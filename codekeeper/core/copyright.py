    def __init__(
            self,
            author: str | None = None,
            license_type: str = "MIT",
            year_range: str | None = None,
            overwrite_mode: OverwriteMode = OverwriteMode.UPDATE_YEAR,
            skip_if_exists: bool = True,
            use_snapshot: bool = True,
    ):
        self.author = author
        self.license = license_type
        self.year_range = year_range
        self.overwrite_mode = overwrite_mode
        self.skip_if_exists = skip_if_exists
        self.use_snapshot = use_snapshot
        self.snapshot_manager = SnapshotManager() if use_snapshot else None

    def _get_handler(self, file_path: Path) -> ILanguageHandler | None:
        return LanguageFactory.get_handler(file_path)

    def _check_file_has_copyright(self, file_path: Path) -> bool:
        handler = self._get_handler(file_path)
        if handler is None:
            return False

        try:
            content = file_path.read_text(encoding="utf-8")
            return handler.has_copyright(content)
        except (OSError, UnicodeDecodeError):
            return False

    def add_copyright(self, file_path: Path) -> CopyrightResult:
        handler = self._get_handler(file_path)
        if handler is None:
            return CopyrightResult(
                file_path=file_path,
                success=False,
                action="skip",
                message=f"No handler found for file type: {file_path.suffix}",
            )

        try:
            content = file_path.read_text(encoding="utf-8")
        except (OSError, UnicodeDecodeError) as e:
            return CopyrightResult(
                file_path=file_path,
                success=False,
                action="error",
                message=f"Failed to read file: {e}",
            )

        if self.skip_if_exists and handler.has_copyright(content) and self.overwrite_mode == OverwriteMode.SKIP:
            return CopyrightResult(
                file_path=file_path,
                success=True,
                action="skip",
                message="File already has copyright header, skipping",
            )

        if self.snapshot_manager:
            self.snapshot_manager.create_snapshot([file_path], "Copyright backup")

        try:
            new_content = handler.insert_copyright(
                content=content,
                license=self.license,
                author=self.author,
                year_range=self.year_range,
            )
            file_path.write_text(new_content, encoding="utf-8")

            action = "overwrite" if handler.has_copyright(content) else "added"
            return CopyrightResult(
                file_path=file_path,
                success=True,
                action=action,
                message=f"Copyright header {action} successfully",
                old_content=content,
                new_content=new_content,
            )
        except (OSError, UnicodeDecodeError) as e:
            if self.snapshot_manager:
                self.snapshot_manager.create_snapshot([file_path], "Copyright backup")
                self.snapshot_manager.rollback_to_latest(file_path)
            return CopyrightResult(
                file_path=file_path,
                success=False,
                action="error",
                message=f"Failed to write file: {e}",
            )

    def remove_copyright(self, file_path: Path) -> CopyrightResult:
        handler = self._get_handler(file_path)
        if handler is None:
            return CopyrightResult(
                file_path=file_path,
                success=False,
                action="skip",
                message=f"No handler found for file type: {file_path.suffix}",
            )

        try:
            content = file_path.read_text(encoding="utf-8")
        except (OSError, UnicodeDecodeError) as e:
            return CopyrightResult(
                file_path=file_path,
                success=False,
                action="error",
                message=f"Failed to read file: {e}",
            )

        if not handler.has_copyright(content):
            return CopyrightResult(
                file_path=file_path,
                success=True,
                action="skip",
                message="No copyright header found, skipping",
            )

        if self.snapshot_manager:
            self.snapshot_manager.create_snapshot([file_path], "Copyright backup")

        lines = content.split("\n")
        new_lines = []
        in_copyright = False
        copyright_started = False

        for line in lines:
            if not copyright_started:
                if "Copyright" in line or "SPDX-License-Identifier" in line:
                    copyright_started = True
                    in_copyright = True
                    continue
                new_lines.append(line)
            elif in_copyright:
                stripped = line.strip()
                if stripped and not stripped.startswith("#") and not stripped.startswith("/"):
                    if "http" not in stripped.lower():
                        in_copyright = False
                        new_lines.append(line)
                elif "Licensed under" in line or "See LICENSE" in line or "SPDX-License" in line or stripped == "":
                    continue
                else:
                    in_copyright = False
                    new_lines.append(line)
            else:
                new_lines.append(line)

        new_content = "\n".join(new_lines).rstrip() + "\n"

        try:
            file_path.write_text(new_content, encoding="utf-8")
            return CopyrightResult(
                file_path=file_path,
                success=True,
                action="removed",
                message="Copyright header removed successfully",
                old_content=content,
                new_content=new_content,
            )
        except (OSError, UnicodeDecodeError) as e:
            if self.snapshot_manager:
                self.snapshot_manager.rollback_to_latest(file_path)
            return CopyrightResult(
                file_path=file_path,
                success=False,
                action="error",
                message=f"Failed to write file: {e}",
            )

    def add_copyright_to_files(
            self,
            file_paths: list[Path],
            extensions: list[str] | None = None,
    ) -> list[CopyrightResult]:
        results = []
        for file_path in file_paths:
            if extensions and file_path.suffix.lstrip(".") not in extensions:
                continue
            result = self.add_copyright(file_path)
            results.append(result)
        return results

    def remove_copyright_from_files(
            self,
            file_paths: list[Path],
            extensions: list[str] | None = None,
    ) -> list[CopyrightResult]:
        results = []
        for file_path in file_paths:
            if extensions and file_path.suffix.lstrip(".") not in extensions:
                continue
            result = self.remove_copyright(file_path)
            results.append(result)
        return results

    def validate_copyright(self, file_path: Path) -> CopyrightResult:
        handler = self._get_handler(file_path)
        if handler is None:
            return CopyrightResult(
                file_path=file_path,
                success=False,
                action="skip",
                message=f"No handler found for file type: {file_path.suffix}",
            )

        try:
            content = file_path.read_text(encoding="utf-8")
            has_copyright = handler.has_copyright(content)

            if has_copyright:
                return CopyrightResult(
                    file_path=file_path,
                    success=True,
                    action="valid",
                    message="Copyright header is valid",
                )
            else:
                return CopyrightResult(
                    file_path=file_path,
                    success=False,
                    action="invalid",
                    message="No copyright header found",
                )
        except (OSError, UnicodeDecodeError) as e:
            return CopyrightResult(
                file_path=file_path,
                success=False,
                action="error",
                message=f"Failed to read file: {e}",
            )
