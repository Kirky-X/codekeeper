class TestAnalysisResponseValidation:
    """分析响应验证测试"""

    @pytest.fixture(autouse=True)
    def setup(self):
        self.framework = CodeKeeperApiTestFramework()
        self.test_dir = self.framework.test_files_dir
        yield
        self.framework.cleanup_test_files()

    def test_analyze_response_contains_file_info(self):
        """测试分析响应包含文件信息"""
        test_file = self.test_dir / "response_test.py"
        test_file.write_text("def test(): pass\n")

        self.framework.register_test_case(
            name="Analyze - Response File Info",
            method=HttpMethod.POST,
            endpoint="analyze",
            description="Verify analyze response contains file info",
            expected_status=200,
            request_data={"file_path": str(test_file)},
            priority="high",
            tags=["analyze", "validation"]
        )

        results = self.framework.run_tests(filter_tags=["validation"])
        assert len(results) == 1
        assert results[0].response_data is not None

    def test_analyze_multiple_files_sequentially(self):
        """测试顺序分析多个文件"""
        files = []
        for i in range(5):
            file_path = self.test_dir / f"seq_{i}.py"
            file_path.write_text(f"def func_{i}(): pass\n")
            files.append(str(file_path))

        for file_path in files:
            self.framework.register_test_case(
                name=f"Analyze Sequential - {file_path.split('/')[-1]}",
                method=HttpMethod.POST,
                endpoint="analyze",
                description=f"Analyze file sequentially",
                expected_status=200,
                request_data={"file_path": file_path},
                priority="medium",
                tags=["analyze", "sequential"]
            )

        results = self.framework.run_tests(filter_tags=["sequential"])
        assert len(results) == len(files)


