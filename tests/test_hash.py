import pytest

from codekeeper.utils.hash import (
    MAX_FILE_SIZE,
    compute_content_hash,
    compute_file_hash,
)


class TestComputeFileHash:
    def test_compute_file_hash_basic(self, tmp_path):
        test_file = tmp_path / "test.txt"
        content = b"Hello, World!"
        test_file.write_bytes(content)

        hash_value = compute_file_hash(test_file)

        expected_hash = compute_content_hash(content)
        assert hash_value == expected_hash
        assert len(hash_value) == 64

    def test_compute_file_hash_empty_file(self, tmp_path):
        test_file = tmp_path / "empty.txt"
        test_file.write_bytes(b"")

        hash_value = compute_file_hash(test_file)

        expected_hash = compute_content_hash(b"")
        assert hash_value == expected_hash
        assert len(hash_value) == 64

    def test_compute_file_hash_large_file(self, tmp_path):
        test_file = tmp_path / "large.txt"
        content = b"x" * (1024 * 1024)
        test_file.write_bytes(content)

        hash_value = compute_file_hash(test_file)

        expected_hash = compute_content_hash(content)
        assert hash_value == expected_hash

    def test_compute_file_hash_file_not_found(self, tmp_path):
        non_existent = tmp_path / "does_not_exist.txt"

        with pytest.raises(FileNotFoundError):
            compute_file_hash(non_existent)

    def test_compute_file_hash_exceeds_max_size(self, tmp_path):
        test_file = tmp_path / "too_large.txt"
        content = b"x" * (MAX_FILE_SIZE + 1)
        test_file.write_bytes(content)

        with pytest.raises(ValueError, match="exceeds maximum allowed size"):
            compute_file_hash(test_file)

    def test_compute_file_hash_custom_chunk_size(self, tmp_path):
        test_file = tmp_path / "chunk_test.txt"
        content = b"a" * 100
        test_file.write_bytes(content)

        hash_4k = compute_file_hash(test_file, chunk_size=4096)
        hash_1k = compute_file_hash(test_file, chunk_size=1024)

        assert hash_4k == hash_1k
        expected_hash = compute_content_hash(content)
        assert hash_4k == expected_hash

    def test_compute_file_hash_string_path(self, tmp_path):
        test_file = tmp_path / "string_path.txt"
        content = b"test content"
        test_file.write_bytes(content)

        hash_value = compute_file_hash(str(test_file))

        expected_hash = compute_content_hash(content)
        assert hash_value == expected_hash

    def test_compute_file_hash_unicode_content(self, tmp_path):
        test_file = tmp_path / "unicode.txt"
        content = "Hello, ‰∏ñÁïå! üåç".encode()
        test_file.write_bytes(content)

        hash_value = compute_file_hash(test_file)

        expected_hash = compute_content_hash(content)
        assert hash_value == expected_hash


class TestComputeContentHash:
    def test_compute_content_hash_basic(self):
        content = b"test content"

        hash_value = compute_content_hash(content)

        assert isinstance(hash_value, str)
        assert len(hash_value) == 64
        assert hash_value == compute_content_hash(content)

    def test_compute_content_hash_empty(self):
        hash_value = compute_content_hash(b"")

        assert len(hash_value) == 64
        assert hash_value == compute_content_hash(b"")

    def test_compute_content_hash_different_inputs(self):
        hash1 = compute_content_hash(b"content1")
        hash2 = compute_content_hash(b"content2")

        assert hash1 != hash2

    def test_compute_content_hash_same_input_same_hash(self):
        content = b"identical content"
        hash1 = compute_content_hash(content)
        hash2 = compute_content_hash(content)

        assert hash1 == hash2


class TestMaxFileSize:
    def test_max_file_size_value(self):
        assert MAX_FILE_SIZE == 10 * 1024 * 1024
        assert MAX_FILE_SIZE == 10485760
