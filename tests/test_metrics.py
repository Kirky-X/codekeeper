class TestOperationMetrics:
    """Tests for OperationMetrics class."""

    def test_record_single_operation(self):
        """Test recording a single operation."""
        metrics = OperationMetrics(operation_name="test_op")
        metrics.record(100.0, success=True)

        assert metrics.call_count == 1
        assert metrics.total_duration_ms == 100.0
        assert metrics.min_duration_ms == 100.0
        assert metrics.max_duration_ms == 100.0
        assert metrics.avg_duration_ms == 100.0
        assert metrics.error_count == 0
        assert metrics.success_rate == 1.0

    def test_record_multiple_operations(self):
        """Test recording multiple operations."""
        metrics = OperationMetrics(operation_name="test_op")

        metrics.record(50.0, success=True)
        metrics.record(100.0, success=True)
        metrics.record(150.0, success=True)
        metrics.record(200.0, success=False)

        assert metrics.call_count == 4
        assert metrics.total_duration_ms == 500.0
        assert metrics.min_duration_ms == 50.0
        assert metrics.max_duration_ms == 200.0
        assert metrics.avg_duration_ms == 125.0
        assert metrics.error_count == 1
        assert metrics.success_rate == 0.75

    def test_percentile_calculation(self):
        """Test percentile calculations."""
        metrics = OperationMetrics(operation_name="test_op")

        for i in range(100):
            metrics.record(float(i + 1), success=True)

        assert metrics.p50_duration_ms == 51.0
        assert metrics.p95_duration_ms == 96.0
        assert metrics.p99_duration_ms == 100.0

    def test_to_dict(self):
        """Test dictionary conversion."""
        metrics = OperationMetrics(operation_name="test_op")
        metrics.record(100.0, success=True)

        result = metrics.to_dict()

        assert result["operation_name"] == "test_op"
        assert result["call_count"] == 1
        assert result["total_duration_ms"] == 100.0
        assert result["success_rate"] == 1.0

    def test_empty_metrics(self):
        """Test empty metrics handling."""
        metrics = OperationMetrics(operation_name="empty_op")

        assert metrics.call_count == 0
        assert metrics.total_duration_ms == 0.0
        assert metrics.avg_duration_ms == 0.0
        assert metrics.success_rate == 0.0
        assert metrics.min_duration_ms == float("inf")
        assert metrics.max_duration_ms == 0.0


