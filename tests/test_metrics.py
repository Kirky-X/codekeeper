class TestPerformanceMonitor:
    """Tests for PerformanceMonitor class."""

    def test_record_operation(self):
        """Test recording operations."""
        monitor = PerformanceMonitor(enabled=True)

        monitor.record_operation("op1", 100.0, success=True)
        monitor.record_operation("op1", 200.0, success=True)
        monitor.record_operation("op2", 50.0, success=False)

        metrics = monitor.get_operation_metric("op1")
        assert metrics.call_count == 2
        assert metrics.error_count == 0

        metrics2 = monitor.get_operation_metric("op2")
        assert metrics2.call_count == 1
        assert metrics2.error_count == 1

    def test_measure_operation_context_manager(self):
        """Test measure_operation context manager."""
        monitor = PerformanceMonitor(enabled=True)

        with monitor.measure_operation("test_op"):
            time.sleep(0.01)

        metrics = monitor.get_operation_metric("test_op")
        assert metrics.call_count == 1
        assert metrics.avg_duration_ms >= 10.0

    def test_measure_operation_with_error(self):
        """Test measure_operation with error handling."""
        monitor = PerformanceMonitor(enabled=True)

        with pytest.raises(ValueError), monitor.measure_operation(
                "error_op", success_on_error=False
        ):
            raise ValueError("Test error")

        metrics = monitor.get_operation_metric("error_op")
        assert metrics.call_count == 1
        assert metrics.error_count == 1

    def test_measure_operation_error_as_success(self):
        """Test measure_operation with success_on_error."""
        monitor = PerformanceMonitor(enabled=True)

        with pytest.raises(ValueError), monitor.measure_operation(
                "error_op", success_on_error=True
        ):
            raise ValueError("Test error")

        metrics = monitor.get_operation_metric("error_op")
        assert metrics.call_count == 1
        assert metrics.error_count == 0

    def test_track_operation_decorator(self):
        """Test track_operation decorator."""
        monitor = PerformanceMonitor(enabled=True)

        @track_operation("decorated_func", monitor=monitor)
        def test_func(x, y):
            return x + y

        result = test_func(1, 2)
        assert result == 3

        metrics = monitor.get_operation_metric("decorated_func")
        assert metrics.call_count == 1

    def test_track_operation_decorator_error(self):
        """Test track_operation decorator with error."""
        monitor = PerformanceMonitor(enabled=True)

        @track_operation("failing_func", monitor=monitor)
        def failing_func():
            raise RuntimeError("Error")

        with pytest.raises(RuntimeError):
            failing_func()

        metrics = monitor.get_operation_metric("failing_func")
        assert metrics.call_count == 1
        assert metrics.error_count == 1

    @pytest.mark.asyncio
    async def test_track_operation_decorator_async(self):
        """Test track_operation decorator with async function."""
        monitor = PerformanceMonitor(enabled=True)

        @track_operation("async_func", monitor=monitor)
        async def async_func():
            await asyncio.sleep(0.01)
            return "done"

        result = await async_func()
        assert result == "done"

        metrics = monitor.get_operation_metric("async_func")
        assert metrics.call_count == 1

    def test_start_end_request(self):
        """Test request timing."""
        monitor = PerformanceMonitor(enabled=True)

        monitor.start_request("req1")
        time.sleep(0.01)
        duration = monitor.end_request("req1")

        assert duration >= 10.0
        assert "req1" not in monitor._request_start_times

    def test_get_summary(self):
        """Test getting performance summary."""
        monitor = PerformanceMonitor(enabled=True)

        monitor.record_operation("op1", 100.0, success=True)
        monitor.record_operation("op2", 50.0, success=False)

        summary = monitor.get_summary()

        assert "operations" in summary
        assert "system" in summary
        assert summary["total_operations"] == 2
        assert summary["total_errors"] == 1

    def test_disabled_monitor(self):
        """Test disabled monitor doesn't record."""
        monitor = PerformanceMonitor(enabled=False)

        monitor.record_operation("op1", 100.0, success=True)

        assert "op1" not in monitor._operation_metrics

        with monitor.measure_operation("op2"):
            time.sleep(0.01)

        assert "op2" not in monitor._operation_metrics

    def test_reset(self):
        """Test resetting metrics."""
        monitor = PerformanceMonitor(enabled=True)

        monitor.record_operation("op1", 100.0, success=True)
        monitor.start_request("req1")

        monitor.reset()

        assert len(monitor._operation_metrics) == 0
        assert len(monitor._request_start_times) == 0

    def test_get_all_operation_metrics(self):
        """Test getting all operation metrics."""
        monitor = PerformanceMonitor(enabled=True)

        monitor.record_operation("op1", 100.0, success=True)
        monitor.record_operation("op2", 200.0, success=True)

        all_metrics = monitor.get_all_operation_metrics()

        assert len(all_metrics) == 2
        operation_names = [m["operation_name"] for m in all_metrics]
        assert "op1" in operation_names
        assert "op2" in operation_names


class TestConvenienceFunctions:
    """Tests for convenience functions."""

    def test_get_monitor_singleton(self):
        """Test get_monitor returns singleton."""
        monitor1 = get_monitor(enabled=True)
        monitor2 = get_monitor()

        assert monitor1 is monitor2

    def test_monitor_operation_context_manager(self):
        """Test monitor_operation convenience function."""
        with monitor_operation("convenience_op"):
            time.sleep(0.01)

        monitor = get_monitor()
        metrics = monitor.get_operation_metric("convenience_op")
        assert metrics.call_count == 1

    def test_track_operation_decorator_default(self):
        """Test track_operation with default monitor."""

        @track_operation("default_decorated")
        def test_func():
            return 42

        result = test_func()
        assert result == 42

        monitor = get_monitor()
        metrics = monitor.get_operation_metric("default_decorated")
        assert metrics.call_count == 1
